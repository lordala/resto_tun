    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String login = t1.getText();
        String pass = t2.getText();
        List<String> rest = new ArrayList<String>();
 if (combo.getSelectedItem() == "admin") {
            CnxDAO cnxdao = new CnxDAO();
            rest = cnxdao.cnxadmin();
            if (rest.contains(login) && rest.contains(pass)) {
                JOptionPane.showMessageDialog(this, "bienvenue");
                this.setVisible(false);
                new menuadmin().setVisible(true);

            } else {
                JOptionPane.showMessageDialog(this, "login ou pass incorect");
                t1.setText("");
                t2.setText("");
            }
        }
 else if (combo.getSelectedItem() == "restaurateur") {
            CnxDAO cnxdao = new CnxDAO();
            rest = cnxdao.cnxRestaurateur();
            if (rest.contains(login) && rest.contains(pass)) {
                JOptionPane.showMessageDialog(this, "bienvenue");
                this.setVisible(false);
                new menurestaurateur().setVisible(true);

            } else {
                JOptionPane.showMessageDialog(this, "login ou pass incorect");
            }
    }//GEN-LAST:event_jButton1ActionPerformed
 else if (combo.getSelectedItem() == "client") {
            CnxDAO cnxdao = new CnxDAO();
            rest = cnxdao.cnxclient();
            if (rest.contains(login) && rest.contains(pass)) {
                JOptionPane.showMessageDialog(this, "bienvenue");
                this.setVisible(false);
                new menuclient().setVisible(true);

            } else {
                JOptionPane.showMessageDialog(this, "login ou pass incorect");
            }
        }
        
    }

